void ExportTile(char*path){
	FILE * fp = fopen(path,"wb");

	for(int i=0; i<NUM_TILE; i++){
		for(int j=0; j<18; j++){
			fprintf(fp,"%c",Tiles[i][j]);
		}
	}

	fclose(fp);
	MessageBox(NULL,"Tiles Exported","Note",MB_OK);
}

